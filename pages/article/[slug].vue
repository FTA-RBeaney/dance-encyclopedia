          <h2 class="font-black text-4xl max-w-[75%] mb-6 mt-14">
            Roots in African-American Vernacular Dance
          </h2>

          <p>
            The Charleston is deeply rooted in **African-American vernacular
            dances**. More specifically, it&rsquo;s an authentic **jazz dance**,
            originally performed to jazz music (initially ragtime), and it
            blends elements derived from improvised African dance with
            syncopated jazz rhythms.
          </p>

          <h2 class="font-black text-4xl max-w-[75%] mb-6 mt-14">
            The Juba Dance: Early Origins
          </h2>

          <p>
            The journey of the Charleston began in the **1800s** with the Juba
            dance, which enslaved Africans brought to Charleston, South
            Carolina. The Juba, also known as Djouba, served as the precursor to
            what we now recognize as the Charleston. Interestingly, the name
            &ldquo;Charleston&rdquo; was later attributed to the Juba dance by
            Europeans after their arrival in America.
          </p>

          <p>
            - **Juba Dance Characteristics**: The Juba, originally referred to
            as **Pattin&rsquo; Juba**, is an African-American dance style
            incorporating one-legged sembuka steps, stamping, slapping the chest
            and arms, and clapping. The term &ldquo;Juba&rdquo; also refers to
            songs sung on plantations.
          </p>

          <p>
            By the **18th century**, the Juba dance was so popular that families
            sought out Black domestics who could teach their daughters the
            latest steps.
          </p>

